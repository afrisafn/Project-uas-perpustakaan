    private void tampil() {
        EntityManager entityManager = Persistence.createEntityManagerFactory("UASPBOPU").createEntityManager();
        entityManager.getTransaction().begin();
        TypedQuery<Skripsi_1> querySelectAll = entityManager.createNamedQuery("Skripsi_1.findAll", Skripsi_1.class);
        List<Skripsi_1> results = querySelectAll.getResultList();

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for (Skripsi_1 data : results) {
            Object[] baris = new Object[5];
            baris[0] = data.getIdSkripsi();
            baris[1] = data.getJudulSkripsi();
            baris[2] = data.getPenulisSkripsi();
            baris[3] = data.getTahunSkripsi();
            baris[4] = data.getHalamanSkripsi();
            model.addRow(baris);
        }
        entityManager.getTransaction().commit();
        entityManager.close();
    }


