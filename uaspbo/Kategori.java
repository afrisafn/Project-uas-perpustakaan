    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Nomer = jTextField1.getText();
        String Kategori = jTextField2.getText();
        
        if (!Kategori.isEmpty()) {

            EntityManager entityManager = Persistence.createEntityManagerFactory("UASPBOPU").createEntityManager();
            entityManager.getTransaction().begin();
            Category c = new Category();
            c.setNomerKategori(Kategori);
            c.setJenis(Nomer);
        
            entityManager.persist(c);
            entityManager.getTransaction().commit();
        } else {
            this.peringatan("Input Data Gagal");
        }
        jTextField1.setText("");
        jTextField2.setText("");
       
        this.tampil();

    }//GEN-LAST:event_jButton1ActionPerformed

