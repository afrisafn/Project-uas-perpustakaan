    private void tampil() {
        EntityManager entityManager = Persistence.createEntityManagerFactory("UASPBOPU").createEntityManager();
        entityManager.getTransaction().begin();
        TypedQuery<Category> querySelectAll = entityManager.createNamedQuery("Category.findAll", Category.class);
        List<Category> results = querySelectAll.getResultList();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Category data : results) {
            Object[] baris = new Object[2];
            baris[0] = data.getNomerKategori();
            baris[1] = data.getJenis();
           
            model.addRow(baris);
        }
        entityManager.getTransaction().commit();
        entityManager.close();
    }
    
    
